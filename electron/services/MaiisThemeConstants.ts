/**
 * 주어진 테마명을 MEGA_THEMES 표준 이름으로 정규화합니다.
 * 1) 정확히 일치하면 그대로 반환
 * 2) 부분 포함으로 매칭 시도
 * 3) FUZZY_MAP으로 키워드 매칭
 * 4) 매칭 실패 시 원본 반환
 */
export function normalizeThemeName(rawName: string): string {
    const name = rawName.trim();
    
    // 1. 정확 매칭
    if ((MEGA_THEMES as readonly string[]).includes(name)) return name;
    
    // 2. MEGA_THEMES 중 부분 포함 매칭 (양방향)
    for (const mt of MEGA_THEMES) {
        if (name.includes(mt) || mt.includes(name)) return mt;
    }
    
    // 3. FUZZY_MAP 키워드 매칭
    for (const [keyword, target] of Object.entries(FUZZY_MAP)) {
        if (name.includes(keyword)) return target;
    }
    
    // 4. 매칭 실패 → 원본 반환 (신규 테마 가능성)
    return name;
}
